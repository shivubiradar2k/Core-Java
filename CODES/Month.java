class Month {
  public static void main(String args[]) {
    int month = 2;
    String value;
    if (month == 1 )
      value = "A";
    else if (month == 2)
      value = "B";
    else if (month == 3)
      value = "C";
    else if (month == 4)
      value = "D";
    else
      value = "Error";

    System.out.println("value = " + value);
  }
}
